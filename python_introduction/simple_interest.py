# simple_interest.py

# 1. Define the input values
principal = 1000        # initial amount invested (P)
rate = 0.05             # annual interest rate as a decimal (R) -> 5% = 0.05
time = 3                # time in years (T)

# 2. Calculate simple interest using the formula I = P * R * T
interest = principal * rate * time

# 3. Print the result in the required format
print(f"The simple interest is: {interest}")
