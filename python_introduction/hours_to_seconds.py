# hours_to_seconds.py

# 1. Define the number of hours to convert
hours = 2  # we’re converting 2 hours

# 2. Calculate the total number of seconds in the given hours
# There are 60 minutes in an hour and 60 seconds in a minute → 60 * 60 = 3600
seconds = hours * 3600

# 3. Display the result
print(f"{hours} hour(s) is {seconds} seconds.")